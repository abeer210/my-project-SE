package com.huawei.hianalytics.ab.bc.cd.bc;

public abstract class bc implements fg {
    public String ab;
    public String bc;
    public String cd;
    public String de;
    public String ef;
    public String fg;

    public void ab(String str) {
        this.fg = str;
    }

    public void bc(String str) {
        this.ab = str;
    }

    public void cd(String str) {
        this.ef = str;
    }

    public void de(String str) {
        this.de = str;
    }

    public void ef(String str) {
        this.cd = str;
    }

    public void fg(String str) {
        this.bc = str;
    }
}
