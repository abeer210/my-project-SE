package com.huawei.hianalytics.ab.bc.cd.ab;

import com.huawei.hianalytics.ab.bc.ef.ab;
import vigqyno.C0201;

public class ef {
    private cd ab;
    private cd bc;
    private cd cd;
    private cd de;

    public ef(String str) {
    }

    public cd ab() {
        return this.cd;
    }

    public cd ab(String str) {
        if (str.equals(C0201.m82(19701))) {
            return cd();
        }
        if (str.equals(C0201.m82(19702))) {
            return bc();
        }
        if (str.equals(C0201.m82(19703))) {
            return ab();
        }
        if (str.equals(C0201.m82(19704))) {
            return de();
        }
        ab.fg(C0201.m82(19706), C0201.m82(19705) + str);
        return null;
    }

    public void ab(cd cdVar) {
        this.cd = cdVar;
    }

    public cd bc() {
        return this.ab;
    }

    public void bc(cd cdVar) {
        this.ab = cdVar;
    }

    public cd cd() {
        return this.bc;
    }

    public void cd(cd cdVar) {
        this.bc = cdVar;
    }

    public cd de() {
        return this.de;
    }

    public void de(cd cdVar) {
        this.de = cdVar;
    }
}
